/**
 * Writes .env.local with SESSION_SECRET + bcrypt auth so local sign-in works.
 * Password hash is stored as base64 so `.env` parsers never mangle `$` in bcrypt strings.
 * Usage: node scripts/create-local-env.cjs [password] [username]
 * Defaults: username dev, password dev
 */
const bcrypt = require('bcryptjs')
const crypto = require('crypto')
const fs = require('fs')
const path = require('path')

const password = process.argv[2] || 'dev'
const username = process.argv[3] || 'dev'
const hash = bcrypt.hashSync(password, 10)
const hashB64 = Buffer.from(hash, 'utf8').toString('base64')
const secret = crypto.randomBytes(32).toString('hex')

const lines = [
  '# Local development — generated by npm run auth:setup',
  '# Do not commit (see .gitignore)',
  '# AUTH_PASSWORD_HASH is base64(bcrypt string) — avoids `$` expansion in .env',
  '',
  `SESSION_SECRET=${secret}`,
  '',
  `AUTH_USERNAME=${username}`,
  `AUTH_PASSWORD_HASH=${hashB64}`,
  '',
  '# Optional: second user → /select-entity after login',
  '# AUTH_MULTI_USERNAME=',
  '# AUTH_MULTI_PASSWORD_HASH=',
  '',
]

const root = path.join(__dirname, '..')
const envPath = path.join(root, '.env.local')

if (fs.existsSync(envPath)) {
  const backup = path.join(root, `.env.local.bak.${Date.now()}`)
  fs.copyFileSync(envPath, backup)
  console.log(`Existing .env.local backed up to ${path.basename(backup)}`)
}

fs.writeFileSync(envPath, lines.join('\n'), 'utf8')
console.log('')
console.log(`Wrote ${envPath}`)
console.log('')
console.log('  Sign in at http://localhost:3000/login with:')
console.log(`    Username: ${username}`)
console.log(`    Password: ${password}`)
console.log('')
console.log('Restart the dev server (npm run dev) if it is already running.')
console.log('')
